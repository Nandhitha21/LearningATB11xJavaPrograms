package ex_10_for_loop;

public class lab87_for_if_loop {
    public static void main(String[] args) {
        for (int shruti =0;shruti<18; shruti++){
            if(shruti>15){
                System.out.println("Gift from pappa");
            }else{
                System.out.println("No gift");
            }
        }
    }
}
