package ex_07_Increment_Decrement;

public class Lab56_POST_ID {
    public static void main(String[] args) {
        int a =10;
        System.out.println(++a);
        System.out.println(a);

        // post increment = print first and increment then increase

        int a1 = 10;

        System.out.println(a1++);
        System.out.println(a1);
    }
}
