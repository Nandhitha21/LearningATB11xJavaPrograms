package ex_05_TypeCasting;

public class Lab43_TypeCasting_0 {
    public static void main(String[] args) {
        byte a = 10;
        int b = a; // implicit widening // valid syntax -> smaller data into the bigger data type

        byte b1 = 10;
        int a1 = (int) b1; // explicit casting -widening (doesn't require)



    }
}
