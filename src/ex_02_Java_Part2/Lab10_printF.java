package ex_02_Java_Part2;

public class Lab10_printF {
    public static void main(String[] args) {

        int a = 10;
        System.out.printf("Your variable is %d", a);

        // %d = int , byte, short- data type
        //%s = string
        // %f = float
        // %b = boolean
        int b = 20;
        System.out.println( " --- ");
        System.out.printf("%d+%d", a, b);
    }
}
